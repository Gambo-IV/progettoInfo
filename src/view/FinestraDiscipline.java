    private void aggiungiButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggiungiButActionPerformed
        // TODO add your handling code here:
        String codice=inputCodice.getText();
        String nome=inputNome.getText();
        String cfu=inputCfu.getText();
        String codicecorso=inputCodiceCorso.getText();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        boolean trovato = false;
        for (int r = 0; r < model.getRowCount(); r++) {
            if (model.getValueAt(r, 0).equals(codice)) { //Se trova un codice uguale, modifica i dati
                // modifica
                model.setValueAt(nome, r, 1);
                model.setValueAt(cfu, r, 2);
                 model.setValueAt(codicecorso, r, 2);
                trovato = true;
                break;
            }
        }
        if (!trovato) { //se non Ã¨ presente
            if (!"".equals(codice) && !"".equals(nome) && !"".equals(cfu) && !"".equals(codicecorso)) { //se i dati sono inseriti
                aggiungiBut.setBackground(Color.white);
                
                model.addRow(new Object[]{codice,nome,cfu,codicecorso});
                try (FileWriter fw = new FileWriter("discipline.csv", true); // true = append
                         PrintWriter pw = new PrintWriter(fw)) {
                    GestoreView.modificaCsv2("discipline.csv", codice, nome, cfu, codicecorso);
                    pw.println(codice + "," + nome + "," + cfu+ "," +codicecorso);
                    System.out.println("Dati aggiunti correttamente!");

                } catch (IOException e) {
                    System.err.println("Errore nella scrittura del file: " + e.getMessage());
                }
            } else { //dati non presenti
                aggiungiBut.setBackground(Color.red);
            }
        }
    }//GEN-LAST:event_aggiungiButActionPerformed

