    private void aggiungiButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggiungiButActionPerformed
        // TODO add your handling code here:
        String id=inputId.getText();
        String matricola=inputMatricola.getText();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
       
        boolean trovato = false;
        for (int r = 0; r < model.getRowCount(); r++) {
        if (model.getValueAt(r, 0).equals(id)) { //Se trova un codice uguale, modifica i dati
            // modifica
            model.setValueAt(matricola, r, 1);
            
            
            try {
                GestoreView.modificaCsv3("iscrizioni.csv",id,matricola);
            } catch (IOException ex) {
                Logger.getLogger(FinestraCorsi.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            trovato = true;
            break;
            }
        }
        if(!trovato){ //se non Ã¨ presente
            if (!"".equals(id) && !"".equals(matricola) ) { //se i dati sono inseriti
            aggiungiBut.setBackground(Color.white);
            model.addRow(new Object[]{id,matricola});
            
            
            try (FileWriter fw = new FileWriter("iscrizioni.csv", true);  // true = append
             PrintWriter pw = new PrintWriter(fw)) {
             GestoreView.modificaCsv3("iscrizioni.csv",id,matricola);
            pw.println(id + "," + matricola);
            System.out.println("Dati aggiunti correttamente!");

        } catch (IOException e) {
            System.err.println("Errore nella scrittura del file: " + e.getMessage());
        }
        } else { //dati non presenti
            aggiungiBut.setBackground(Color.red);
        }
        }
    }//GEN-LAST:event_aggiungiButActionPerformed

